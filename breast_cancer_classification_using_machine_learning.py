alpha = 0.05
imp = SimpleImputer(strategy='median')
X_imp = pd.DataFrame(imp.fit_transform(x_1), columns=x_1.columns)

"""#### 3.3.1 Chi‚ÄëSquare Test

* Null Hypothesis (H‚ÇÄ): There is no association between the binned texture_difference (e.g. Low/Medium/High) and the diagnosis (benign vs. malignant).

* Alternative Hypothesis (H‚ÇÅ): There is an association between the binned texture_difference and diagnosis.

* Conclusion: If the computed p‚Äëvalue < 0.05, we reject H‚ÇÄ ‚Äî implying a statistically significant link between texture_difference category and cancer class.
"""

# 1Ô∏è‚É£ Chi‚ÄëSquare Test on binned texture_difference
disc = KBinsDiscretizer(n_bins=3, encode='ordinal', strategy='uniform')
binned = disc.fit_transform(X_imp[['texture_difference']]).astype(int).ravel()
contingency = pd.crosstab(binned, y)
chi2_stat, chi2_p, _, _ = chi2_contingency(contingency)
print(f"Chi‚ÄëSquare: p = {chi2_p:.3e} ‚Üí {'reject H0' if chi2_p < alpha else 'fail to reject H0'}")

"""#### 3.3.2 T‚ÄëTest

* Null Hypothesis (H‚ÇÄ): There is no significant difference in the mean texture_difference between the two diagnosis groups (0 = benign, 1 = malignant).

* Alternative Hypothesis (H‚ÇÅ): There is a significant difference in the mean texture_difference between benign and malignant tumors.

* Conclusion: A p‚Äëvalue < 0.05 ‚Üí reject H‚ÇÄ, indicating the average texture_difference differs significantly across classes.
"""

# 2Ô∏è‚É£ T‚ÄëTest for each feature
g0 = X_imp[y == 0]
g1 = X_imp[y == 1]
for feat in X_imp.columns:
    t_stat, t_p = ttest_ind(g0[feat], g1[feat], nan_policy='omit')
    print(f"T‚ÄëTest ({feat}): p = {t_p:.3e} ‚Üí {'reject H0' if t_p < alpha else 'fail to reject H0'}")

